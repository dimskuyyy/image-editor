    <div class="title--page">
        <h3>Tint</h3>
    </div>
    <div class="article--content">
        <p>Tint adalah jenis atau nama warna yang tercampur dengan unsur warna putih. Warna tint juga biasa disebut sebagai warna pastel atau warna lembut. Warna tint terlihat lebih cerah namun tidak terlihat lebih terang. Lebih tepatnya warna tint merupakan versi warna pucat pada jenis warna hue. Untuk menciptakan cat berwarna tint cukup dengan menambahkan warna putih pada cat yang berwarna natural (hue).

        </p>
    </div>

    <div class="title--page">
        <h3>Multiply</h3>
    </div>
    <div class="article--content">
        <p>Membuat channel warna yang diinginkan menjadi lebih menonjol sehingga warna tersebut lebih terhlihat jelas pada gambar</p>
    </div>

    <div class="title--page">
        <h3>Blend</h3>
    </div>
