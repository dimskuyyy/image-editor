        <h3>Remove White</h3>
    </div>
    <div class="article--content">
        <p>Remove White adalah fungsi untuk menghilangkan warna putih pada gambar</p>
    </div>

    <div class="title--page">
        <h3>Brightness</h3>
    </div>
    <div class="article--content">
        <p>Brightness adalah pengaturan gelap terangnya suatu gambar.</p>
    </div>

    <div class="title--page">
        <h3>Noise</h3>
    </div>
    <div class="article--content">
        <p>Noise adalah sebuah istilah dalam dunia fotografi untuk menyebut titik-titik berwarna yang biasanya menganggu hasil foto sehingga membuat foto menjadi tidak tampak halus </p>
    </div>

    <div class="title--page">
        <h3>Pixelate</h3>
    </div>
    <div class="article--content">
        <p>Pixelate atau pikselasi adalah efek yang terjadi ketika sebuah gambar bitmat ditampilkan dalam ukuran begitu besar sehingga pixel-pixel yang ada dalam gambar tersebut tampak sebagai kotak-kotak berwarna. Pixelate juga dapat digunakan untuk menyamarkan isi gambar. </p>
    </div>

    <div class="title--page">
        <h3>Color Filter - transparant</h3>
    </div>
    <div class="article--content">
        <p>Membuat warna dominan menjadi transparant </p>
    </div>

