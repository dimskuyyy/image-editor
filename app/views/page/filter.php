    <div class="title--page">
        <h3>Sepia</h3>
    </div>
    <div class="article--content">
        <p>Grayscale adalah berbagai nuansa warna monokromatik dari hitam menjadi putih. Oleh karena itu, gambar grayscale hanya memiliki warna abu-abu dan tidak berwarna. Sebuah gambar digital dapat disimpan sebagai grayscale (hitam dan putih), bahkan gambar berwarna berisi informasi grayscale. Hal ini karena setiap pixel memiliki nilai luminance, terlepas dari warna. Luminance juga dapat digambarkan sebagai kecerahan atau intensitas, yang dapat diukur pada skala dari hitam (nol intensitas) ke putih (intensitas penuh). </p>
    </div>

    <div class="title--page">
        <h3>Blur</h3>
    </div>
    <div class="article--content">
        <p>Blur adalah metode filter yang memperhalus sebuah gambar atau tampilan grafis sehingga memberikan kesan buram. Blur biasa digunakan untuk mengurangi detail atau memperluas tampilan. Blur juga bisa diartikan sebagai kekaburan seuluruh atau sebagian gambar karena gerakan yang disengaja atau tidak disengaja pada saat pemotretan dan efek besar kecilnya diagfragma. Hal ini terjadi saat melakukan teknik panning atau zooming yang menggunakan kecepatan rendah. </p>
    </div>

    <div class="title--page">
        <h3>Emboss</h3>
    </div>
    <div class="article--content">
        <p>Emboss adalah sebuah proses finishing yang membuat hasil cetak menjadi timbul keluar. Kedalam dari emboss bisa diatur sesuai dengan kebutuhan. Dalam dunia printing, proses emboss dilakukan dengan menekan kertas dari bawah, sehingga memberikan efek 3D yang menonjol

        </p>
    </div>

    <div class="title--page">
        <h3>Invert</h3>
    </div>
    <div class="article--content">
        <p>Invert adalah membuat sebuah efek negatif film dari sebuah gambar</p>
    </div>

    <div class="title--page">
        <h3>Sharpen</h3>
    </div>
    <div class="article--content">
        <p>Sharpen adalah penajaman gambar dengan meningkatkan kontras pixel gelap dan terang. Pixel gelap dibuat lebih gelap dan pixel terang dibuat lebih terang </p>
    </div>

    <hr style="height:2px; border-radius:1px; background-color:#5f6266; margin:70px 0px 70px;width:100%">

